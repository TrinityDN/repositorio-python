print("Nos informe as seguintes informações de um retângulo:")
base = float(input("Digite o tamanho de sua base: "))
altura = float(input("Digite o tamanho de sua altura: "))

area = base * altura
perimetro = 2 * (base+altura)

print("A área do retângulo é de: ", " ", area)
print("O perímetro do retângulo é de: ", " ", perimetro)


